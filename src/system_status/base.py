class StatusTimestampMixin:
    """Provides timestamp field for status snapshots."""

    @declared_attr
    def timestamp(cls):
        return Column(DateTime, nullable=False, index=True)

    @declared_attr
    def created_at(cls):
        return Column(DateTime, nullable=False, default=utcnow_naive,
                     server_default=text('CURRENT_TIMESTAMP'))


class StatusSnapshotMixin(StatusTimestampMixin):
    """
    Mixin for time-series status tables.
    Provides timestamp indexing and snapshot identification.
    """
    pass


class AvailabilityMixin:
    """Provides availability status tracking."""

    @declared_attr
    def available(cls):
        return Column(Boolean, nullable=False, default=True)

    @declared_attr
    def degraded(cls):
        return Column(Boolean, nullable=False, default=False)

    @property
    def is_available(self) -> bool:
        """Check if resource is fully available."""
        return bool(self.available and not self.degraded)

    @property
    def status_name(self) -> str:
        """Get human-readable status name."""
        if not self.available:
            return "offline"
        elif self.degraded:
            return "degraded"
        else:
            return "online"


class SessionMixin:
    """Provides access to SQLAlchemy session."""

    @property
    def session(self) -> Session:
        s = Session.object_session(self)
        return s


class QueueRollupMetricsMixin:
    """Per-queue rollup metric columns shared by ``QueueStatus`` and
    ``UserProjQueueStatus``.

    The two tables capture the same counters at different aggregation grains:
    ``QueueStatus`` collapses across users/projects, ``UserProjQueueStatus``
    keys by ``(user, project_code, queue)``. Keeping the columns in a mixin
    avoids drift if a new metric is added.

    ``active_users`` is intentionally NOT part of the mixin — it's only
    meaningful at the queue grain (one row per user, by definition, in the
    per-user table).
    """

    @declared_attr
    def running_jobs(cls):
        return Column(Integer, nullable=False, default=0)

    @declared_attr
    def pending_jobs(cls):
        return Column(Integer, nullable=False, default=0)

    @declared_attr
    def held_jobs(cls):
        return Column(Integer, nullable=False, default=0)

    @declared_attr
    def cores_allocated(cls):
        return Column(Integer, nullable=False, default=0)

    @declared_attr
    def gpus_allocated(cls):
        return Column(Integer, nullable=False, default=0)

    @declared_attr
    def nodes_allocated(cls):
        return Column(Integer, nullable=False, default=0)

    @declared_attr
    def cores_pending(cls):
        return Column(Integer, nullable=False, default=0)

    @declared_attr
    def gpus_pending(cls):
        return Column(Integer, nullable=False, default=0)

    @declared_attr
    def cores_held(cls):
        return Column(Integer, nullable=False, default=0)

    @declared_attr
    def gpus_held(cls):
        return Column(Integer, nullable=False, default=0)
