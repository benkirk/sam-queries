# ============================================================================
# Naming convention — stable across MySQL, Postgres, and SQLite.
#
# Alembic autogenerate emits explicit constraint names from these templates,
# so migrations are portable across dialects. Applies to the standalone
# declarative_base() used by `alembic` (FLASK_ACTIVE unset). Existing
# models already use explicit constraint names; this convention only
# affects newly autogenerated names going forward.
# ============================================================================
STATUS_NAMING_CONVENTION = {
    "ix": "ix_%(table_name)s_%(column_0_name)s",
    "uq": "uq_%(table_name)s_%(column_0_N_name)s",
    "ck": "ck_%(table_name)s_%(constraint_name)s",
    "fk": "fk_%(table_name)s_%(column_0_name)s_%(referred_table_name)s",
    "pk": "pk_%(table_name)s",
}


